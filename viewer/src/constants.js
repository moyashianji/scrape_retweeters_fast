/**
 * ユーザーが特定メンバーのファンかどうかを判定する共通関数
 * 検索対象: bio + name + quote_text(A) + location(F)
 * 判定基準: mentions / keywords / fanMark絵文字(B)
 * @returns {string[]} マッチしたメンバー名の配列
 */
export function detectFanOf(user, members) {
  // A: quote_text も対象, F: location も対象
  const raw = [user.bio || '', user.name || '', user.quote_text || '', user.location || ''].join(' ')
  const lower = raw.toLowerCase()

  const matched = []
  Object.entries(members).forEach(([name, data]) => {
    const hasMention = data.mentions.some(acc => lower.includes(`@${acc.toLowerCase()}`))
    const hasKeyword = data.keywords.some(kw => lower.includes(kw.toLowerCase()))
    // B: ファンマーク絵文字判定（fanEmojis配列 or 単一fanMark）
    const emojis = data.fanEmojis || (data.fanMark ? [data.fanMark] : [])
    const hasFanMark = emojis.some(emoji => raw.includes(emoji))
    if (hasMention || hasKeyword || hasFanMark) matched.push(name)
  })
  return matched
}

export function parseFollowerCount(str) {
  if (!str) return 0
  const num = parseFloat(String(str).replace(/,/g, ''))
  if (String(str).includes('K')) return num * 1000
  if (String(str).includes('M')) return num * 1000000
  if (String(str).includes('B')) return num * 1000000000
  return num || 0
}

export function mergeUser(existing, incoming, fileName) {
  const merged = { ...existing }
  const fields = ['name', 'bio', 'verified', 'profile_image_url',
                  'location', 'url', 'followers_count', 'following_count', 'can_dm',
                  'statuses_count', 'favourites_count', 'media_count', 'listed_count',
                  'created_at', 'is_blue_verified', 'protected', 'default_profile_image',
                  'profile_banner_url', 'rest_id']
  for (const f of fields) {
    if (!merged[f] && incoming[f]) {
      merged[f] = incoming[f]
    }
  }
  if (incoming.quote_text) {
    if (merged.quote_text && merged.quote_text !== incoming.quote_text) {
      merged.quote_text = merged.quote_text + '\n---\n' + incoming.quote_text
    } else if (!merged.quote_text) {
      merged.quote_text = incoming.quote_text
    }
  }
  const sources = new Set(merged._sources || [])
  sources.add(fileName)
  merged._sources = [...sources]
  return merged
}
